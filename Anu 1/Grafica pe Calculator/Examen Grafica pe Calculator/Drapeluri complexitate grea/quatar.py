from turtle import*
import turtle as t
from math import sqrt

Screen().bgcolor("gray")

speed(0)
penup()
goto(-200,100)
pendown()

color("#8A1538")
begin_fill()
forward(400)
right(90)
forward(200)
right(90)
forward(400)
right(90)
forward(200)
end_fill()

penup()
goto(-200, 100)
setheading(0)
pendown()

color("white")
begin_fill()
forward(100)
right(90)
forward(200)
right(90)
forward(100)
right(90)
forward(200)
end_fill()

penup()
goto(-100, 100)
setheading(0)
pendown()

x = sqrt(22 ** 2 / 5)

right(30)
for i in range(9):
  begin_fill()
  forward(2.25 * x)
  right(120)
  forward(2.25 * x)
  left(120)
  end_fill()

penup()
setheading(0)
left(180)
color("brown")
begin_fill()
goto(-200,100)
pendown()
forward(30)
left(90)
forward(500)
left(90)
forward(30)
left(90)
forward(500)
end_fill()
penup()
color("black")
goto(0, -300)
write("Quatar", font=("Arial", 48, "normal"))
t.done()
