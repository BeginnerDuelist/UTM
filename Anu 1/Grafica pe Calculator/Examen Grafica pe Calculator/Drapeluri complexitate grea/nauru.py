from turtle import*
import turtle as t

Screen().bgcolor("gray")

speed(0)
penup()
goto(-200,100)
pendown()

color("#012169")
begin_fill()
forward(400)
right(90)
forward(200)
right(90)
forward(400)
right(90)
forward(200)
end_fill()

penup()
goto(-200, 100 - 11 * 8.33)
setheading(0)
pendown()

color("#FFC72C")
begin_fill()
forward(400)
right(90)
forward(8.33 * 2)
right(90)
forward(400)
right(90)
forward(8.33 * 2)
end_fill()

penup()
goto(-200 + 7 * 8.33, 100 - 18.5 * 8.33)
setheading(0)
pendown()

color('white')
begin_fill()
for i in range(12):
    forward(15)
    right(110)
    forward(15.5)
    left(140)
end_fill()

penup()
setheading(0)
left(180)
color("brown")
begin_fill()
goto(-200,100)
pendown()
forward(30)
left(90)
forward(500)
left(90)
forward(30)
left(90)
forward(500)
end_fill()
penup()
color("black")
goto(0, -300)
write("Nauru", font=("Arial", 48, "normal"))
t.done()
